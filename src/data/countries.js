export function getCountryFlag(code) {
  return code
    .toUpperCase()
    .split('')
    .map(char => String.fromCodePoint(127397 + char.charCodeAt(0)))
    .join('');
}

export function getPhoneHint(code) {
  const c = COUNTRIES.find(x => x.code === code);
  if (!c) return null;
  const [min, max] = c.phoneDigits;
  return min === max ? `${min} digits required` : `${min}–${max} digits required`;
}

export function getPhonePlaceholder(code) {
  const c = COUNTRIES.find(x => x.code === code);
  if (!c) return 'Phone number';
  const [min] = c.phoneDigits;
  return '0'.repeat(min);
}